async def get_document_history(tenant_id: str = None, limit: int = 20) -> List[Dict]:
    db = _get_db()
    if not db:
        return []
    q = {"tenant_id": tenant_id} if tenant_id else {}
    return await db.generated_documents.find(q, {"_id": 0}).sort("created_at", -1).limit(limit).to_list(limit)
