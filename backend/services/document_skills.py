# ═══════════════════════════════════════════════════════════════
# DOCX GENERATION — Proposals, Welcome Packages, Outreach
# ═══════════════════════════════════════════════════════════════

async def generate_docx(
    title: str,
    sections: List[Dict],
    doc_type: str = "proposal",
    metadata: Dict = None,
    tenant_id: str = "aurem_platform",
) -> Dict:
    """
    Generate branded DOCX document.
    sections: [{"heading": "...", "content": "...", "style": "normal|bullet|table"}]
    """
    from docx import Document
    from docx.shared import Inches, Pt, Cm, RGBColor
    from docx.enum.text import WD_ALIGN_PARAGRAPH
    from docx.enum.section import WD_ORIENT

    doc = Document()

    # Page setup
    section = doc.sections[0]
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin = Cm(2.5)
    section.right_margin = Cm(2.5)

    # Title
    title_p = doc.add_paragraph()
    title_p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    run = title_p.add_run(title)
    run.font.size = Pt(28)
    run.font.color.rgb = RGBColor(0x2D, 0x2A, 0x2E)
    run.font.bold = True
    run.font.name = "Calibri"

    # Accent line
    line_p = doc.add_paragraph()
    line_run = line_p.add_run("━" * 60)
    line_run.font.color.rgb = RGBColor(0xD4, 0xAF, 0x37)
    line_run.font.size = Pt(8)

    # Metadata
    if metadata:
        meta_p = doc.add_paragraph()
        meta_p.alignment = WD_ALIGN_PARAGRAPH.LEFT
        for k, v in metadata.items():
            run = meta_p.add_run(f"{k}: {v}\n")
            run.font.size = Pt(10)
            run.font.color.rgb = RGBColor(0x88, 0x88, 0x88)

    doc.add_paragraph()  # spacer

    # Sections
    for sec in sections:
        heading = sec.get("heading", "")
        content = sec.get("content", "")
        style = sec.get("style", "normal")

        if heading:
            h = doc.add_heading(heading, level=2)
            for run in h.runs:
                run.font.color.rgb = RGBColor(0x2D, 0x2A, 0x2E)
                run.font.name = "Calibri"

        if style == "bullet" and isinstance(content, list):
            for item in content:
                p = doc.add_paragraph(str(item), style="List Bullet")
                for run in p.runs:
                    run.font.size = Pt(11)
        elif style == "table" and isinstance(content, list) and content:
            cols = list(content[0].keys()) if isinstance(content[0], dict) else ["Item"]
            table = doc.add_table(rows=1, cols=len(cols))
            table.style = "Light Grid Accent 1"
            for i, col in enumerate(cols):
                table.rows[0].cells[i].text = str(col).title()
            for row_data in content:
                row = table.add_row()
                if isinstance(row_data, dict):
                    for i, col in enumerate(cols):
                        row.cells[i].text = str(row_data.get(col, ""))
                else:
                    row.cells[0].text = str(row_data)
        else:
            p = doc.add_paragraph(str(content))
            for run in p.runs:
                run.font.size = Pt(11)
                run.font.name = "Calibri"

    # Footer
    doc.add_paragraph()
    footer_p = doc.add_paragraph()
    footer_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = footer_p.add_run("Generated by AUREM AI — Sovereign Business Automation")
    run.font.size = Pt(8)
    run.font.color.rgb = RGBColor(0xD4, 0xAF, 0x37)
    run.font.italic = True

    # Save
    doc_id = f"doc_{secrets.token_hex(6)}"
    fpath = f"{DOC_DIR}/{doc_id}.docx"
    doc.save(fpath)

    db = _get_db()
    if db:
        await db.generated_documents.insert_one({
            "doc_id": doc_id, "type": doc_type, "format": "docx", "title": title,
            "tenant_id": tenant_id, "file_path": fpath, "sections": len(sections),
            "created_at": datetime.now(timezone.utc).isoformat(),
        })

    return {"doc_id": doc_id, "format": "docx", "file_path": fpath, "title": title, "generated": True}


# ═══════════════════════════════════════════════════════════════
# PPTX GENERATION — Health Reports, Campaign Decks
# ═══════════════════════════════════════════════════════════════

async def generate_pptx(
    title: str,
    slides: List[Dict],
    doc_type: str = "report",
    tenant_id: str = "aurem_platform",
) -> Dict:
    """
    Generate branded PPTX presentation.
    slides: [{"title": "...", "content": "...", "layout": "title|content|two_col|metrics"}]
    """
    from pptx import Presentation
    from pptx.util import Inches, Pt, Emu
    from pptx.dml.color import RGBColor
    from pptx.enum.text import PP_ALIGN

    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    # Title Slide
    slide_layout = prs.slide_layouts[6]  # blank
    slide = prs.slides.add_slide(slide_layout)

    # Background
    bg = slide.background
    fill = bg.fill
    fill.solid()
    fill.fore_color.rgb = RGBColor(0x2D, 0x2A, 0x2E)

    # Title text
    txBox = slide.shapes.add_textbox(Inches(1), Inches(2), Inches(11), Inches(2))
    tf = txBox.text_frame
    p = tf.paragraphs[0]
    p.text = title
    p.font.size = Pt(44)
    p.font.color.rgb = RGBColor(0xF5, 0xF5, 0xF5)
    p.font.bold = True
    p.alignment = PP_ALIGN.LEFT

    # Accent line
    line = slide.shapes.add_shape(1, Inches(1), Inches(4.2), Inches(3), Emu(36000))
    line.fill.solid()
    line.fill.fore_color.rgb = RGBColor(0xD4, 0xAF, 0x37)
    line.line.fill.background()

    # Subtitle
    txBox2 = slide.shapes.add_textbox(Inches(1), Inches(4.5), Inches(8), Inches(1))
    tf2 = txBox2.text_frame
    p2 = tf2.paragraphs[0]
    p2.text = f"Generated by AUREM AI — {datetime.now(timezone.utc).strftime('%B %Y')}"
    p2.font.size = Pt(16)
    p2.font.color.rgb = RGBColor(0xD4, 0xAF, 0x37)

    # Content slides
    for sl in slides:
        slide = prs.slides.add_slide(prs.slide_layouts[6])  # blank
        bg = slide.background
        fill = bg.fill
        fill.solid()
        fill.fore_color.rgb = RGBColor(0xF5, 0xF5, 0xF5)

        # Slide title
        txBox = slide.shapes.add_textbox(Inches(0.8), Inches(0.4), Inches(11), Inches(1))
        tf = txBox.text_frame
        p = tf.paragraphs[0]
        p.text = sl.get("title", "")
        p.font.size = Pt(28)
        p.font.color.rgb = RGBColor(0x2D, 0x2A, 0x2E)
        p.font.bold = True

        # Content
        content = sl.get("content", "")
        if isinstance(content, list):
            content = "\n".join(f"• {item}" for item in content)

        txBox2 = slide.shapes.add_textbox(Inches(0.8), Inches(1.6), Inches(11), Inches(5))
        tf2 = txBox2.text_frame
        tf2.word_wrap = True
        p2 = tf2.paragraphs[0]
        p2.text = str(content)
        p2.font.size = Pt(16)
        p2.font.color.rgb = RGBColor(0x44, 0x44, 0x44)

    # Save
    doc_id = f"pptx_{secrets.token_hex(6)}"
    fpath = f"{DOC_DIR}/{doc_id}.pptx"
    prs.save(fpath)

    db = _get_db()
    if db:
        await db.generated_documents.insert_one({
            "doc_id": doc_id, "type": doc_type, "format": "pptx", "title": title,
            "tenant_id": tenant_id, "file_path": fpath, "slides": len(slides) + 1,
            "created_at": datetime.now(timezone.utc).isoformat(),
        })

    return {"doc_id": doc_id, "format": "pptx", "file_path": fpath, "title": title, "slides": len(slides) + 1, "generated": True}


# ═══════════════════════════════════════════════════════════════
# PDF GENERATION — Formal Reports
# ═══════════════════════════════════════════════════════════════

async def generate_pdf(
    title: str,
    sections: List[Dict],
    doc_type: str = "report",
    tenant_id: str = "aurem_platform",
) -> Dict:
    """Generate branded PDF report via reportlab."""
    from reportlab.lib.pagesizes import letter
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.lib.units import inch
    from reportlab.lib.colors import HexColor
    from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle

    doc_id = f"pdf_{secrets.token_hex(6)}"
    fpath = f"{DOC_DIR}/{doc_id}.pdf"

    doc = SimpleDocTemplate(fpath, pagesize=letter, topMargin=0.75*inch, bottomMargin=0.75*inch)
    styles = getSampleStyleSheet()

    title_style = ParagraphStyle("AuremTitle", parent=styles["Title"], fontSize=24, textColor=HexColor("#2D2A2E"), spaceAfter=6)
    heading_style = ParagraphStyle("AuremH2", parent=styles["Heading2"], fontSize=16, textColor=HexColor("#2D2A2E"), spaceBefore=16, spaceAfter=8)
    body_style = ParagraphStyle("AuremBody", parent=styles["Normal"], fontSize=11, textColor=HexColor("#333333"), spaceAfter=8, leading=16)
    accent_style = ParagraphStyle("AuremAccent", parent=styles["Normal"], fontSize=9, textColor=HexColor("#D4AF37"), spaceAfter=4)

    elements = []
    elements.append(Paragraph(title, title_style))
    elements.append(Paragraph("━" * 80, accent_style))
    elements.append(Spacer(1, 12))

    for sec in sections:
        heading = sec.get("heading", "")
        content = sec.get("content", "")
        style = sec.get("style", "normal")

        if heading:
            elements.append(Paragraph(heading, heading_style))

        if style == "bullet" and isinstance(content, list):
            for item in content:
                elements.append(Paragraph(f"• {item}", body_style))
        elif style == "table" and isinstance(content, list) and content:
            if isinstance(content[0], dict):
                cols = list(content[0].keys())
                data = [cols] + [[str(row.get(c, "")) for c in cols] for row in content]
            else:
                data = [[str(c)] for c in content]
            t = Table(data)
            t.setStyle(TableStyle([
                ("BACKGROUND", (0, 0), (-1, 0), HexColor("#2D2A2E")),
                ("TEXTCOLOR", (0, 0), (-1, 0), HexColor("#FFFFFF")),
                ("FONTSIZE", (0, 0), (-1, -1), 10),
                ("GRID", (0, 0), (-1, -1), 0.5, HexColor("#CCCCCC")),
                ("ROWBACKGROUNDS", (0, 1), (-1, -1), [HexColor("#FFFFFF"), HexColor("#F9F9F9")]),
            ]))
            elements.append(t)
        else:
            elements.append(Paragraph(str(content), body_style))

        elements.append(Spacer(1, 8))

    elements.append(Spacer(1, 24))
    elements.append(Paragraph("Generated by AUREM AI — Sovereign Business Automation", accent_style))

    doc.build(elements)

    db = _get_db()
    if db:
        await db.generated_documents.insert_one({
            "doc_id": doc_id, "type": doc_type, "format": "pdf", "title": title,
            "tenant_id": tenant_id, "file_path": fpath, "sections": len(sections),
            "created_at": datetime.now(timezone.utc).isoformat(),
        })

    return {"doc_id": doc_id, "format": "pdf", "file_path": fpath, "title": title, "generated": True}


