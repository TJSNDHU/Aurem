"""
ORA Dev Actions Router (iter 281.2 — Phase 2.2)
================================================
Admin-gated CRUD over `db.ora_dev_actions` — the queue of code-change
proposals generated by ORA's Mode 2 (Software Engineer) brain.

Contract:
  - GET  /api/admin/ora-dev/pending           list pending proposals
  - GET  /api/admin/ora-dev/list?status=...   filtered list (last 100)
  - GET  /api/admin/ora-dev/stats             counts by status
  - POST /api/admin/ora-dev/{id}/approve      mark approved
  - POST /api/admin/ora-dev/{id}/reject       mark rejected
  - POST /api/admin/ora-dev/{id}/applied      mark applied (admin did it)
  - POST /api/admin/ora-dev/{id}/rollback     mark rolled back
  - GET  /api/admin/ora-dev/health            public liveness probe

Hard rule: ORA NEVER auto-applies code. Status transitions only.
Admin still does the actual file editing manually (or uses the
existing pending_code_fixes pipeline for tier-3 auto-heal).
"""
from __future__ import annotations

import logging
from datetime import datetime, timezone
from typing import Any, Dict, Optional

from fastapi import APIRouter, Header, HTTPException, Query

from utils.admin_guard import verify_admin

logger = logging.getLogger(__name__)
router = APIRouter(prefix="/api/admin/ora-dev", tags=["ORA Dev Console"])

_db = None
_VALID_STATUSES = {"pending", "approved", "rejected", "applied", "rolled_back"}


def set_db(db):
    global _db
    _db = db


def _get_db():
    return _db


def _serialize(doc: Dict[str, Any]) -> Dict[str, Any]:
    """Strip MongoDB _id before JSON return."""
    if not doc:
        return {}
    out = {k: v for k, v in doc.items() if k != "_id"}
    return out


@router.get("/health")
async def health():
    db = _get_db()
    return {"ok": True, "db_wired": db is not None}


@router.get("/pending")
async def list_pending(authorization: Optional[str] = Header(None)):
    verify_admin(authorization)
    db = _get_db()
    if db is None:
        raise HTTPException(503, "Database not wired")
    cursor = (
        db.ora_dev_actions
        .find({"status": "pending"})
        .sort("created_at", -1)
        .limit(50)
    )
    items = [_serialize(d) async for d in cursor]
    return {"ok": True, "count": len(items), "items": items}


@router.get("/list")
async def list_actions(
    authorization: Optional[str] = Header(None),
    status: Optional[str] = Query(None),
    limit: int = Query(50, ge=1, le=200),
):
    verify_admin(authorization)
    db = _get_db()
    if db is None:
        raise HTTPException(503, "Database not wired")
    q: Dict[str, Any] = {}
    if status:
        if status not in _VALID_STATUSES:
            raise HTTPException(400, f"Invalid status (allowed: {sorted(_VALID_STATUSES)})")
        q["status"] = status
    cursor = db.ora_dev_actions.find(q).sort("created_at", -1).limit(limit)
    items = [_serialize(d) async for d in cursor]
    return {"ok": True, "count": len(items), "items": items}


@router.get("/stats")
async def stats(authorization: Optional[str] = Header(None)):
    verify_admin(authorization)
    db = _get_db()
    if db is None:
        raise HTTPException(503, "Database not wired")
    counts = {s: 0 for s in _VALID_STATUSES}
    pipeline = [{"$group": {"_id": "$status", "n": {"$sum": 1}}}]
    async for row in db.ora_dev_actions.aggregate(pipeline):
        s = row.get("_id") or "pending"
        counts[s] = int(row.get("n", 0))
    counts["total"] = sum(counts.values())
    return {"ok": True, **counts}


async def _transition(
    proposal_id: str,
    *,
    new_status: str,
    admin_email: Optional[str],
    extra: Optional[Dict[str, Any]] = None,
    allowed_from: Optional[set] = None,
) -> Dict[str, Any]:
    db = _get_db()
    if db is None:
        raise HTTPException(503, "Database not wired")
    doc = await db.ora_dev_actions.find_one({"proposal_id": proposal_id})
    if not doc:
        raise HTTPException(404, f"Proposal {proposal_id} not found")
    if allowed_from and doc.get("status") not in allowed_from:
        raise HTTPException(
            409,
            f"Cannot transition from `{doc.get('status')}` to `{new_status}`",
        )
    update = {
        "status": new_status,
        f"{new_status}_at": datetime.now(timezone.utc).isoformat(),
        f"{new_status}_by": admin_email or "admin",
    }
    if new_status == "approved":
        update["approved_at"] = update[f"{new_status}_at"]
        update["approved_by"] = admin_email or "admin"
    if extra:
        update.update(extra)
    await db.ora_dev_actions.update_one(
        {"proposal_id": proposal_id}, {"$set": update}
    )
    refreshed = await db.ora_dev_actions.find_one({"proposal_id": proposal_id})
    return _serialize(refreshed or {})


@router.post("/{proposal_id}/approve")
async def approve(proposal_id: str, authorization: Optional[str] = Header(None)):
    payload = verify_admin(authorization)
    if (payload.get("sealed_blocked") or False) is True:
        # cosmetic: not relevant on token, kept for symmetry
        pass
    doc = await _transition(
        proposal_id,
        new_status="approved",
        admin_email=payload.get("email"),
        allowed_from={"pending"},
    )
    if doc.get("sealed_blocked"):
        raise HTTPException(409, "Proposal touches sealed file(s) — cannot approve")
    # iter 282e — browser-action proposals auto-execute on approve.
    # Other proposal kinds (code change, ORA action, etc.) stay at
    # "approved" and wait for their existing applier paths.
    if doc.get("kind") == "browser_action":
        try:
            from services.browser_agent_service import execute_approved_action
            exec_result = await execute_approved_action(proposal_id)
            doc["execution_result"] = exec_result
        except Exception as e:
            doc["execution_result"] = {"ok": False, "error": str(e)[:200]}
    # iter 322 — auto_bridge proposals carry a structured `approve_action`
    # the executor knows how to run server-side (e.g., run_migration_iter322,
    # mark_safe_apply). When admin clicks Approve, fire the action and
    # transition status to "applied" + capture the audit payload.
    elif doc.get("source") == "auto_bridge" and doc.get("approve_action"):
        try:
            from services.ora_proposal_bridge import execute_approve_action
            db = _get_db()
            exec_result = await execute_approve_action(db, doc["approve_action"])
            doc["execution_result"] = exec_result
            if exec_result.get("ok"):
                await db.ora_dev_actions.update_one(
                    {"proposal_id": proposal_id},
                    {"$set": {
                        "status": "applied",
                        "applied_at": datetime.now(timezone.utc).isoformat(),
                        "applied_payload": exec_result,
                    }},
                )
                doc["status"] = "applied"
                doc["applied_at"] = datetime.now(timezone.utc).isoformat()
                doc["applied_payload"] = exec_result
        except Exception as e:
            doc["execution_result"] = {"ok": False, "error": str(e)[:200]}
    return {"ok": True, "proposal": doc}


