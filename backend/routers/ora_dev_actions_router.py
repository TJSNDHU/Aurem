# ─── iter 322s — Tier-1 auto-approval cancel window ─────────────────
@router.post("/{proposal_id}/cancel-auto")
async def cancel_auto_approval(
    proposal_id: str, authorization: Optional[str] = Header(None),
):
    """Cancel a tier_1 proposal during its 5-minute auto-execute window.

    Only succeeds if:
      - status == "pending"
      - tier == "tier_1"
      - auto_execute_at is in the future (still in cancel window)
    """
    payload = verify_admin(authorization)
    db = _get_db()
    if db is None:
        raise HTTPException(503, "Database not wired")
    existing = await db.ora_dev_actions.find_one({"proposal_id": proposal_id})
    if not existing:
        raise HTTPException(404, "Proposal not found")
    if existing.get("status") != "pending":
        raise HTTPException(409, f"Cannot cancel — current status is {existing.get('status')}")
    if existing.get("tier") != "tier_1":
        raise HTTPException(409, "Cancel-auto is tier_1 only — use /reject for tier_2")
    aex = existing.get("auto_execute_at")
    if aex and isinstance(aex, datetime) and aex < datetime.now(timezone.utc):
        raise HTTPException(409, "Auto-execute window has already elapsed")
    await db.ora_dev_actions.update_one(
        {"proposal_id": proposal_id},
        {"$set": {
            "status": "cancelled",
            "cancelled_by": payload.get("email"),
            "cancelled_at": datetime.now(timezone.utc).isoformat(),
        }},
    )
    refreshed = await db.ora_dev_actions.find_one({"proposal_id": proposal_id})
    return {"ok": True, "proposal": _serialize(refreshed or {})}


# ─── iter 322t — Plain-Hinglish translation (retro-fill for old proposals) ─
@router.post("/{proposal_id}/translate")
async def translate_proposal(
    proposal_id: str, authorization: Optional[str] = Header(None),
):
    """Retro-translate an existing proposal's request_text + proposal_text
    into plain Hinglish. Useful for proposals created before iter 322t
    that don't yet carry `plain_language`. Idempotent — overwrites any
    existing translation."""
    verify_admin(authorization)
    db = _get_db()
    if db is None:
        raise HTTPException(503, "Database not wired")
    existing = await db.ora_dev_actions.find_one({"proposal_id": proposal_id})
    if not existing:
        raise HTTPException(404, "Proposal not found")

    from services.ora_proposal_bridge import (
        _classify_safety_level, _translate_to_plain_language,
    )
    action_kind = ((existing.get("approve_action") or {}).get("kind")
                   if existing.get("approve_action") else None)
    # Always recompute fresh on retro-translate — don't reuse stale values.
    safety_level = _classify_safety_level(
        action_kind,
        existing.get("source_kind"),
        existing.get("request_text") or "",
        existing.get("proposal_text") or "",
    )
    plain = await _translate_to_plain_language(
        request_text=existing.get("request_text") or "",
        proposal_text=existing.get("proposal_text") or "",
        severity=existing.get("severity") or "P2",
        action_kind=action_kind,
        safety_level=safety_level,
    )
    await db.ora_dev_actions.update_one(
        {"proposal_id": proposal_id},
        {"$set": {
            "plain_language": plain,
            "safety_level": safety_level,
            "translated_at": datetime.now(timezone.utc).isoformat(),
        }},
    )
    refreshed = await db.ora_dev_actions.find_one({"proposal_id": proposal_id})
    return {"ok": True, "proposal": _serialize(refreshed or {})}


# ─── iter 281.3 — Apply via PR (Phase 2.3 add-on) ──────────────────
def _slugify(s: str, n: int = 32) -> str:
    import re
    s = re.sub(r"[^a-zA-Z0-9]+", "-", (s or "").lower()).strip("-")
    return s[:n] or "ora-mode2"


def _extract_target_files(proposal_text: str) -> list:
    """Best-effort parse of TARGET FILE(S) section from Mode 2 prompt output."""
    import re
    lines = (proposal_text or "").splitlines()
    files = []
    in_target = False
    for ln in lines:
        if "TARGET FILE" in ln.upper():
            in_target = True
            continue
        if in_target:
            t = ln.strip().strip("`").strip("*").strip()
            if not t or t.startswith("#"):
                continue
            if t.upper().startswith(("INTENT", "REASONING", "SUGGESTED", "RISKS", "TEST")):
                break
            # Strict: must look like a path — no spaces, has extension,
            # short enough to be a file path (not a sentence).
            if " " in t or len(t) > 200:
                continue
            if not re.search(r"\.[a-zA-Z]{1,6}$", t):
                continue
            if t.startswith("/") or t.startswith(("backend/", "frontend/", "app/")) or "/" in t:
                files.append(t)
    return files[:10]


@router.post("/{proposal_id}/prepare-pr")
async def prepare_pr(proposal_id: str, authorization: Optional[str] = Header(None)):
    """Generate a GitHub-friendly commit message + branch suggestion for an
    approved Mode 2 proposal. Does NOT push — admin uses the platform's
    'Save to GitHub' feature with the suggested commit message.

    The commit message uses the `[ora-mode2]` prefix (parallels the existing
    `[auto-heal]` convention) so the deploy workflow can identify it.
    """
    payload = verify_admin(authorization)
    db = _get_db()
    if db is None:
        raise HTTPException(503, "Database not wired")
    doc = await db.ora_dev_actions.find_one({"proposal_id": proposal_id})
    if not doc:
        raise HTTPException(404, f"Proposal {proposal_id} not found")
    if doc.get("sealed_blocked"):
        raise HTTPException(409, "Sealed-blocked proposal — cannot prepare PR")
    if doc.get("status") not in ("approved", "applied"):
        raise HTTPException(
            409,
            f"Status `{doc.get('status')}` — approve the proposal first before preparing a PR",
        )

    request_text = (doc.get("request_text") or "")[:80]
    intent_short = _slugify(request_text or doc.get("proposal_id", ""), 32)
    branch = f"ora-mode2/{proposal_id[:8]}-{intent_short}"
    commit_msg = f"[ora-mode2] {request_text}".strip()
    if len(commit_msg) > 100:
        commit_msg = commit_msg[:97] + "..."

    target_files = _extract_target_files(doc.get("proposal_text") or "")
    pr_body = (
        f"Auto-generated by ORA Mode 2 (Sovereign Brain).\n\n"
        f"**Proposal ID**: `{proposal_id}`\n"
        f"**Approved by**: {doc.get('approved_by') or 'admin'}\n\n"
        f"**Original request**:\n> {doc.get('request_text', '')}\n\n"
        f"**Proposal**:\n```\n{(doc.get('proposal_text') or '')[:3000]}\n```\n"
    )

    await db.ora_dev_actions.update_one(
        {"proposal_id": proposal_id},
        {"$set": {
            "pr_prepared_at": datetime.now(timezone.utc).isoformat(),
            "pr_prepared_by": payload.get("email") or "admin",
            "pr_branch_suggested": branch,
            "pr_commit_message": commit_msg,
        }},
    )

    return {
        "ok": True,
        "proposal_id": proposal_id,
        "branch": branch,
        "commit_message": commit_msg,
        "target_files": target_files,
        "pr_body": pr_body,
        "next_step": (
            "Copy the commit_message, then click 'Save to GitHub' in the chat "
            "input to push. The [ora-mode2] prefix is parsed by the deploy "
            "workflow."
        ),
    }
